void sh1107_draw_bitmap(uint8_t x, uint8_t y, const uint8_t *bitmap, uint8_t w, uint8_t h) {    
    for (uint8_t j = 0; j < h; j++) {
        for (uint8_t i = 0; i < w / 8; i++) {  // 8 pixels per byte
            uint8_t byte = bitmap[(h - 1 - j) * (w / 8) + i];  // ðŸ”„ Flip vertically
            for (uint8_t k = 0; k < 8; k++) {
                if (byte & (1 << (7 - k))) {
                    sh1107_draw_pixel(x + (i * 8) + k, y + j, 1);
                } else {
                    sh1107_draw_pixel(x + (i * 8) + k, y + j, 0);
                }
            }
        }
    }
}
